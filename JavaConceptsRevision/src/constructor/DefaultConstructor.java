package constructor;

public class DefaultConstructor {
	String student_name  ;
	int Rollno ;
	
	public static void main(String[] args) {
		
		DefaultConstructor student = new DefaultConstructor(); 
		
		
		System.out.println(student.student_name);
		System.out.println(student.Rollno);
		
		
	}
}

		
	
	


