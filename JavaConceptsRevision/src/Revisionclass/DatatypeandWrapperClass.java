package Revisionclass;

public class DatatypeandWrapperClass {

	public static void main(String[] args) {
		// Primitive Data Types
		System.out.println("Primitive Data Type");
		int num = 10;
		char ch = 'j';
		long acnum = 1234567890l;
		float fl = 12345f;
		System.out.println("int:" + num);
		System.out.println("long:" + acnum);
		System.out.println("float:" + fl);
		System.out.println("char:" + ch);
		// NonPrimitive Data Types
		System.out.println("===================");
		System.out.println("Non Primitive-Wrapper Class");
		String str = "Hello";
		Integer acbal = 500;
		Long ac_num = 12345678901l;
		System.out.println("String:" + str);
		System.out.println("Integer:" + acbal);
		System.out.println("Long:" + ac_num);

	}

}
